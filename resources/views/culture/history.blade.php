        <p>
            Топонимика объясняет нам все более просто. Гдов - гидроним от названия реки Гдова, где окончание «ва»
            означает в переводе с финно-угорского «вода», а начало слова «гда» - «лесная» или «черная», таким
            образом
            Гдова - лесная вода.
        </p><br>
        <p>
            Гдов впервые упоминается в Первой Псковской летописи осенью 1322 года от Рождества Христова, когда в
            очередной раз Ливонский орден напал на русские земли у Чудского озера, у рек Нарвы и Чермы:
        </p><br>
        <p class="italic text-gray-500"> «В лето 6831. Приеха князь великий Георгий с Низу во Псков, и прияша его
            псковичи с честию от всего
            сердца. Toe же осени избиша немцы пскович на миру, и гостей во озере, и ловцов на Норове реце; а берег
            весь и городок Гдову, Череместь взяша; и послаша псковичи к Давыду князю в Литву, и Давыд князь приеха
            на сыропустной недели в четверток, а князь Георгий еще бяше в Пскове; и еха князь со псковичи за Нерову
            и плени землю немецкую до Колываня; а князь великий Георгий поеха изо Пскова в Новгород».</p><br>

        <p> В других псковских летописях при рассказе об этом набеге упоминание о Гдове отсутствует. </p><br>

        <p>Весной 1431 года при князе Дмитрии Александровиче Ростовском и псковских посадниках Якиме Павловиче
            Княжичеве, Феодосии Феофиловиче и Стефане на реке Гдове была заложена новая крепость.</p>
