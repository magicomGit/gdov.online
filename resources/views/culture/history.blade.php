            <p class="italic text-gray-500"> «В лето 6939. Того же лета, на весну, псковичи наяша мастеров 300 муж и
                заложиша город новый, на реце, на Гдове, половину стены камену, а иную половину древяну, при князи
                Димитрии Александровичи и при посаднике Якиме Княжичеве; и единого лета оучиниша каменую и древяную
                стену, и земцы безкии даша мастером 300 рублев».</p><br>
            <p>
                Крепость расположилась на плоской вершине холма и имела форму почти правильного прямоугольника. С
                северо-востока и северо-запада холм омывается водами реки Гдовки, с юго-запада - ручьём Старицей. Перед
                юго-восточной (приступной) стеной был вырыт глубокий ров, заполнявшийся водой. Самая крупная башня -
                Петелинская, имела пять ярусов бойниц, расположенных в шахматном порядке. Из этой башни за пределы
                крепости шел под землей "слух" - параллельный стене длинный ход, благодаря которому можно было узнать
                место вражеских подкопов. На западе крепости у Псковской (Круглой) башни находились её главные ворота —
                Псковские. По центру северо-западной стены находилась Середняя (Тайницкая) башня с тайным ходом к реке
                Гдовке, на севере находились Маленские (Малые) ворота. На востоке располагались третьи ворота —
                Кушельские. На южной оконечности крепости стояла угловая Костерская башня. Стены достигали высоты 5,5-8
                метров, а шириной были 3,5-5 метров. Для духовной поддержки гарнизона в крепости была построена
                Успенская церковь.
            </p><br>

            <p>
                Гарнизон Гдова не раз отражал набеги немецких, польских и шведских войск. В 1480 г. Ливонские рыцари
                осадили крепость, но не взяв её сожгли посад: «В лето 6988. Тоя же зимы, генваря в 20 день, приидоша
                немци с многыми силами к Гдову городу; и начаша огненыя стрелы на град пущати, и бяше велми притужно
