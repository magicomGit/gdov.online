            </p><br>

            <p>После Северной войны 1700-1721 годов Гдов перестает быть пограничной крепостью, потеряв стратегическое
                значение.</p><br>
            <p>
                С 1708 года Гдов - уездный центр в составе Санкт-Петербургской (Ингерманландской) губернии, с 1727 года
                - в составе Псковской провинции Новгородской губернии, с 1777 года - в составе Псковского
                наместничества, с 1781 года - вновь в составе Санкт-Петербургской губернии, с 1927 года - в
                Ленинградской области, с 1944 года - в составе Псковской области.
                19 июля 1941 года Гдов был захвачен немецкими войсками и сильно разрушен.
            </p><br>
