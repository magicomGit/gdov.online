            <p>
                Решение о строительстве Гдовской крепости было принято на псковском вече весной 1431 года. Начало
                строительства пришлось на «5 недели по велике дни» (очевидно, пятая неделя после Пасхи — с 30 апреля (9
                мая) по 6 (15) мая). К 1 (10) ноября крепость была готова. С приступной (наиболее уязвимой) стороны она
                была каменная, с других сторон — деревянная. За работу местные Березские земцы заплатили мастерам 300
                рублей.
